#!/usr/bin/env node
// Build a single-file bundle with the wasm inlined as base64.
// Output: dist/capnwasm.bundle.mjs
//
// This is the "fair" cold-start size comparison: one network fetch, no
// separate .wasm round-trip.

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { resolve } from "node:path";
import { gzipSync } from "node:zlib";

const ROOT = resolve(new URL("..", import.meta.url).pathname);

const wasm = await readFile(resolve(ROOT, "zig-out", "capnwasm.opt.wasm"));
const indexJs = await readFile(resolve(ROOT, "js", "index.mjs"), "utf8");
const tapeJs = await readFile(resolve(ROOT, "js", "tape.mjs"), "utf8");

const wasmB64 = wasm.toString("base64");

// Inline tape.mjs into index.mjs by replacing the import.
const indexInlined = indexJs.replace(
  /import\s*\{\s*TapeWriter\s*,\s*TapeReader\s*\}\s*from\s*"\.\/tape\.mjs";\s*/,
  "",
);

const bundle = `// capnwasm: single-file bundle with inlined wasm.
// Generated by js/bundle.mjs — do not edit by hand.

const WASM_BASE64 = "${wasmB64}";

function base64ToBytes(b64) {
  if (typeof Buffer !== "undefined") return new Uint8Array(Buffer.from(b64, "base64"));
  const bin = atob(b64);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

${tapeJs.replace(/^export /gm, "")}

${indexInlined.replace(/^export /gm, "")}

const __wasmBytes = base64ToBytes(WASM_BASE64);
export async function load() { return CapnWasm.load(__wasmBytes); }
export { CapnWasm, TapeWriter, TapeReader };
`;

await mkdir(resolve(ROOT, "dist"), { recursive: true });
await writeFile(resolve(ROOT, "dist", "capnwasm.bundle.mjs"), bundle);

const raw = Buffer.byteLength(bundle, "utf8");
const gz = gzipSync(bundle, { level: 9 }).length;
console.log(`dist/capnwasm.bundle.mjs:  raw=${raw}  gzip=${gz}`);

// Compare with capnweb
const capnwebJs = await readFile(resolve(ROOT, "..", "capnweb", "dist", "index.js"));
const capnwebGz = gzipSync(capnwebJs, { level: 9 }).length;
console.log(`capnweb/dist/index.js:     raw=${capnwebJs.length}  gzip=${capnwebGz}`);

const ratio = (gz / capnwebGz).toFixed(2);
console.log(`\ncapnwasm/capnweb gzip ratio (single-file): ${ratio}x`);
